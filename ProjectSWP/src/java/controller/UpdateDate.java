    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession session = request.getSession();
        String url = request.getParameter("url");
        String checkin_raw = request.getParameter("checkin");
        String checkout_raw = request.getParameter("checkout");
        Date checkin = Date.valueOf(checkin_raw);
        Date checkout = Date.valueOf(checkout_raw);
        checkin.compareTo(checkout);
        LocalDate nextDate1 = LocalDate.parse(checkin_raw);
        LocalDate nextDate2 = nextDate1.plusDays(1);
        if (checkin.compareTo(checkout) == 1 || checkin.compareTo(checkout) == 0) {
            checkout = Date.valueOf(nextDate2);
        }
        Date max = Date.valueOf(nextDate2);
        session.setAttribute("max", max);
        session.setAttribute("checkin", checkin);
        session.setAttribute("checkout", checkout);
        response.sendRedirect(url);
    }

