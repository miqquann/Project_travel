        String id_raw = request.getParameter("id");
        String status_raw = request.getParameter("status");
        int id = Integer.parseInt(id_raw);
        boolean status = Boolean.parseBoolean(status_raw);
        List<Room> list = rd.getRoomByHotelId(id);
