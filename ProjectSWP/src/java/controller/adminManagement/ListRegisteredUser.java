    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /** 
     * Handles the HTTP <code>GET</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        UserDAO u = new UserDAO();
        int page = 1;
        int userReInPage = 3;
        
        if (request.getParameter("page") != null) {
            page = Integer.parseInt(request.getParameter("page"));
        }

        if (request.getParameter("userReInPage") != null) {
            userReInPage = Integer.parseInt(request.getParameter("userReInPage"));
        }
        
        List<User> list = u.getRegisteredUser(page,userReInPage );
        int totalSuppliers = u.getTotalRegisteredUser();
        int totalPages = (int) Math.ceil((double) totalSuppliers / userReInPage);
        request.setAttribute("listRegisteredUser", list);
        request.setAttribute("currentPage", page);
        request.setAttribute("totalPages", totalPages);
        request.setAttribute("recordsPerPage", userReInPage);
        request.getRequestDispatcher("listRegisteredUser.jsp").forward(request, response);
    } 

    /** 
     * Handles the HTTP <code>POST</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        processRequest(request, response);
    }

